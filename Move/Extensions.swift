import UIKit

extension UIButton{
    func categoryButton(){
        self.layer.cornerRadius = 22.5
        self.layer.borderWidth = 1
        self.layer.borderColor = UIColor(red:0.81, green:0.80, blue:0.80, alpha:1.0).cgColor
    }
}
